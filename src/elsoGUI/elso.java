    private void szamolActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_szamolActionPerformed
       
        negyedikszam = Double.parseDouble(kepletjel.getText());
        if ((abevitel.getText().equals(""))|| (bbevitel.getText().equals("")))
        {
        JOptionPane.showMessageDialog(null, "Adjon meg értékeket a számításhoz.",
                "Képletszámító", JOptionPane.INFORMATION_MESSAGE);
        }
        //út=sebesség*idő
        else if (kepletvalaszto.getSelectedItem().equals("Út"))
        {
            double harmadikszam = Double.parseDouble(abevitel.getText());
            double negyedikszam = Double.parseDouble(abevitel.getText());
            
            kiszamol = harmadikszam * negyedikszam;
        }
                
            kepletjel.setText(String.valueOf(kiszamol));
        
        
        
        
        
        
    }//GEN-LAST:event_szamolActionPerformed

