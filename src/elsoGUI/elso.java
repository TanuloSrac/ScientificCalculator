    private void egyenloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_egyenloActionPerformed
        // TODO add your handling code here:
        masodikszam = Double.parseDouble(kijelzo.getText());
        
                if (muvelet.equals("/")){
            eredmeny = elsoszam / masodikszam;
        }
                if (muvelet.equals("*")){
            eredmeny = elsoszam * masodikszam;
        }
                if (muvelet.equals("+")){
            eredmeny = elsoszam + masodikszam;
        }
                if (muvelet.equals("-")){
            eredmeny = elsoszam - masodikszam;
        }
        
                kijelzo.setText(String.valueOf(eredmeny));
        
    }//GEN-LAST:event_egyenloActionPerformed

